package com.jbwang.tree;

public class Node {
	
	public int iData;
	public double dData;
	public Node leftChild;  //左子节点
	public Node rightChild; //右子节点
	
	public Node(int iData, double dData) {
		this.iData = iData;
		this.dData = dData;
	}
	
	public void displayNode() {
		System.out.println("{");
		System.out.println(iData);
		System.out.println(",");
		System.out.println(dData);
		System.out.println("}");
	}

}
